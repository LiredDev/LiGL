class Vector2 {
    public:
        int X;
        int Y;

        Vector2(int x, int y) {
            X = x;
            Y = y;
        };
};

class Vector3 {
    public:
        int X;
        int Y;
        int Z;

        Vector3(int x, int y, int z) {
            X = x;
            Y = y;
            Z = z;
        };
};

class Vector2f {
    public:
        float X;
        float Y;

        Vector2f(float x, float y) {
            X = x;
            Y = y;
        };
};

class Vector3f {
    public:
        float X;
        float Y;
        float Z;

        Vector3f(float x, float y, float z) {
            X = x;
            Y = y;
            Z = z;
        };
};